// keyword(let,var,const)= pre defined

// let variable_name=value; 
let a=10;
let b=20;
let c=a+b;
console.log(c);
let d=a-b;
console.log(d);
let e=a*b;
console.log(e);
let f=a/b;
console.log(f);

// let string_name='Character';
let name= 'Aryan ';
console.log(name);

let surname= 'Pachhai';
console.log(surname);

let fullname=name +surname;
console.log(fullname);

// gives NaN(Not a Number)
let fullnames=name-surname;
console.log(fullnames);

//'const' gives constant value for a variable
const pi=3.1428;
console.log(pi);

// 'var' is used to declare variable 
var x=11;
var x=12;
var y=12;
var z=x-y;
console.log(z);

/**
 * 'var' variables can be updated and re-declared
 * 'let' variables can be updated but not re-declared
 * 'const' variables can neither be updated nor re-declared
 */

