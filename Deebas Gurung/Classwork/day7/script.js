// unit conversion
let distance=Number(prompt("Enter the distance covered in kilometer"));
let meter=(distance*1000);
let hectometer=(distance*10);
let decameter=(distance*100);
let milimeter=(distance*10**6);
let centimeter=(distance*10**5);
let mile=(distance*0.621371);
let yard=(distance*1093.61);
let foot=(distance*3280.84);
let inch=(distance*39,370.1);
